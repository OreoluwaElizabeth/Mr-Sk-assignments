pie = 3.14159

radius = 2

diameter = 2 * radius

circumference = 2 * pie * radius

area = (pie * radius) ** (pie * radius)

print(f" The diameter of the circle is: {diameter}")

print(f" The circumference of the circle is: {circumference:.2f}")

print(f" The area of the circle is: {area:.2f}")
